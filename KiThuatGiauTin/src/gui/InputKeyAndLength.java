    private void executeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_executeBtnActionPerformed
        // TODO add your handling code here:
        // Nhập 2 chuỗi
        String keyIn = keyField.getText().trim();
        String lengthIn = lengthField.getText().trim();
        // Nếu khóa trống
        if (keyIn.isEmpty()){
            JOptionPane.showMessageDialog(this, "Key is null", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        // Nếu độ dài trống
        else if (lengthIn.isEmpty()){
            JOptionPane.showMessageDialog(this, "Set length to retrieve messages", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try{
                // Nếu độ dài sai format number
                int length = Integer.parseInt(lengthIn);
                if (length <= 0) throw new NumberFormatException();
                else{
                    this.keyString = keyIn;
                    this.length = length;
                    this.everyThingSet = true;
                    this.dispose();
                }
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Length must be positive integer bigger than 0", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_executeBtnActionPerformed

